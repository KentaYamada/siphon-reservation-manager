import { tel } from "@/plugins/validate";

/**
 * Custom validation rules test
 */
describe("validate", () => {
  it.each([
    "06-1-234",
    "06-1-2345",
    "06-12-345",
    "06-12-3456",
    "06-123-456",
    "06-123-4567",
    "06-1234-567",
    "06-1234-5678",
    "090-1-234",
    "090-1-2345",
    "090-12-345",
    "090-12-3456",
    "090-123-456",
    "090-123-4567",
    "090-1234-567",
    "090-1234-5678",
    "0120-1-234",
    "0120-1-2345",
    "0120-12-345",
    "0120-12-3456",
    "0120-123-456",
    "0120-123-4567",
    "0120-1234-567",
    "0120-1234-5678"
  ])("tel rule test", (value: string) => {
    expect(tel(value)).toBeTruthy();
  });

  it.each(["", "abc", "0-1-2", "0--3456", "090--1234", "090-1-2", "090-12-12"])(
    "test tel when passed invalid value",
    (value: string) => {
      expect(tel(value)).toBeFalsy();
    }
  );
});
